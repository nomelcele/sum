package vo;

public class MessengerVO {
	private int mesnum, mesmem;
	private String mescont, mesreceiver;
	
	protected int getMesnum() {
		return mesnum;
	}
	protected void setMesnum(int mesnum) {
		this.mesnum = mesnum;
	}
	protected int getMesmem() {
		return mesmem;
	}
	protected void setMesmem(int mesmem) {
		this.mesmem = mesmem;
	}
	protected String getMescont() {
		return mescont;
	}
	protected void setMescont(String mescont) {
		this.mescont = mescont;
	}
	protected String getMesreceiver() {
		return mesreceiver;
	}
	protected void setMesreceiver(String mesreceiver) {
		this.mesreceiver = mesreceiver;
	}
	
	

}
